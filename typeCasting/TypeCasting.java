package typeCasting;

public class TypeCasting {

	public static void main(String[] args) {
		//Automatic Type Casting
		int x = 4;
		double y =x;
		System.out.println(y);
		
		//Manual type casting
		
		double myDouble = 2.8462;
		int myInt = (int)myDouble;
		
		System.out.println(myDouble);
		System.out.println(myInt); 
		 
		

	}

}
