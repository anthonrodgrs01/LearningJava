package operators;

public class AssignmentOperators {
	public static void main(String[] args) {
		
		//Equal to operation
		int a = 4;
		System.out.println(a);
		
		//a+=6; ---> a=a+6;
		a+=6;
		System.out.println(a);
		
		a-=6;
		System.out.println(a);
		
		a*=5;
		System.out.println(a);
		
		a/=5;
		System.out.println(a);
		
		a>>=1;
		System.out.println(a);
		
		a<<=2;
		System.out.println(a);
	}

}
